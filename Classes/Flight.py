class Flight:
    def __init__(location, initialDate, returnDate):
        self.location = location
        self.initialDate = initialDate
        self.returnDate = returnDate

    def getLocation():
        return self.location

    def setLocation(location):
        self.location = location
    
    def getInitialDate():
        return initialDate
    
    def setInitialDate(initialDate):
        self.initialDate = initialDate

    def getReturnDate():
        return returnDate

    def setReturnDate(returnDate):
        self.returnDate = returnDate
        